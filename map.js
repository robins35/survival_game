var init = function(assetImgs) {
    this.imgs = assetImgs;
    this.imgMapping = {
        0 : this.imgs.grass,
        1 : this.imgs.dirt,
        2 : this.imgs.water,
        3 : this.imgs.mushroom,
        4 : this.imgs.tree
    };
    this.tileSize = this.imgMapping[0].width;
    this.height = layers[0].length * this.tileSize;
    this.width = layers[0].length * this.tileSize;
}

var getImageAt = function(layer_num, pos) {
    //  console.log("ERROR: YOU REQUESTED AN IMAGE OUTSIDE THE MAP!");
    //  return false;
    var layer = layers[layer_num];
    var y = Math.floor(pos.y / this.tileSize);
    var x = Math.floor(pos.x / this.tileSize);
    if (y >= layer.length) y = layer.length - 1;
    if (x >= layer[0].length) x = layer[0].length - 1;
    return this.imgMapping[layer[y][x]];
}

